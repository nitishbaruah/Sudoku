    private void CheckMovesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CheckMovesActionPerformed
        // TODO add your handling code here:
            JButton bts[][]={{R1B1,R1B2,R1B3,R2B1,R2B2,R2B3,R3B1,R3B2,R3B3},
                           {R1B4,R1B5,R1B6,R2B4,R2B5,R2B6,R3B4,R3B5,R3B6},
                           {R1B7,R1B8,R1B9,R2B7,R2B8,R2B9,R3B7,R3B8,R3B9},
                           {R4B1,R4B2,R4B3,R5B1,R5B2,R5B3,R6B1,R6B2,R6B3},
                           {R4B4,R4B5,R4B6,R5B4,R5B5,R5B6,R6B4,R6B5,R6B6},
                           {R4B7,R4B8,R4B9,R5B7,R5B8,R5B9,R6B7,R6B8,R6B9},
                           {R7B1,R7B2,R7B3,R8B1,R8B2,R8B3,R9B1,R9B2,R9B3},
                           {R7B4,R7B5,R7B6,R8B4,R8B5,R8B6,R9B4,R9B5,R9B6},
                           {R7B7,R7B8,R7B9,R8B7,R8B8,R8B9,R9B7,R9B8,R9B9} };
            boolean flag=true;
            for(int i=0;i<9;i++){
                for(int j=0;j<9;j++){
                    if(solvedBoard[i][j]!=bts[i][j].getText() && bts[i][j].getText()!=""){
                        bts[i][j].setBackground(red);
                        flag=false;
                    }
                }
            }
            if(flag){
                for(int i=0;i<9;i++){
                for(int j=0;j<9;j++){
                    if(bts[i][j].getText()==""){
                        //break;
                        flag=false;
                        break;
                    }
                }
                if(flag==false) break;
            }
            }
            if(flag){
                JOptionPane.showMessageDialog(this, "You Won", "Message", JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_CheckMovesActionPerformed

    private void solutionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solutionActionPerformed
        // TODO add your handling code here:
        JButton predefined[]= {R1B4,R1B7,R1B9,R2B1,R2B5,R2B6,R2B8,R3B1,R3B2,R3B3,R3B4,R3B6,R3B8,R4B5,R4B6,R4B8,R5B2,R5B3,R5B7,R5B8,R6B2,R6B4,R6B5,R7B2,R7B4,R7B6,R7B7,R7B8,R7B9,R8B2,R8B4,R8B5,R8B9,R9B1,R9B3,R9B6};
                               
        JButton bts[][]={{R1B1,R1B2,R1B3,R2B1,R2B2,R2B3,R3B1,R3B2,R3B3},
                           {R1B4,R1B5,R1B6,R2B4,R2B5,R2B6,R3B4,R3B5,R3B6},
                           {R1B7,R1B8,R1B9,R2B7,R2B8,R2B9,R3B7,R3B8,R3B9},
                           {R4B1,R4B2,R4B3,R5B1,R5B2,R5B3,R6B1,R6B2,R6B3},
                           {R4B4,R4B5,R4B6,R5B4,R5B5,R5B6,R6B4,R6B5,R6B6},
                           {R4B7,R4B8,R4B9,R5B7,R5B8,R5B9,R6B7,R6B8,R6B9},
                           {R7B1,R7B2,R7B3,R8B1,R8B2,R8B3,R9B1,R9B2,R9B3},
                           {R7B4,R7B5,R7B6,R8B4,R8B5,R8B6,R9B4,R9B5,R9B6},
                           {R7B7,R7B8,R7B9,R8B7,R8B8,R8B9,R9B7,R9B8,R9B9} };
        
        if(globalflag==true){
            solution.setText("HIDE SOLUTIONS");
            globalflag=false;
            for(int i=0;i<9;i++){
                for(int j=0;j<9;j++){
                    boolean flag=true;
                    for(int k=0;k<predefined.length;k++){
                        if(bts[i][j]==predefined[k]) flag=false;
                        
                    }
                    if(flag){
                        bts[i][j].setText(solvedBoard[i][j]);
                        bts[i][j].setBackground(green);
                    }
                }
            }
        }
        else{
            solution.setText("SOLUTIONS");
            globalflag=true;
            reset();
            
        }
        
    }//GEN-LAST:event_solutionActionPerformed

